package concurrency.generator.frontend.flowchart.util;

public class CoordinatesXY {

	private int x;
	
	private int y;
	
	public CoordinatesXY(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return this.x;
	}
	
	public int getY() {
		return this.y;
	}
}
